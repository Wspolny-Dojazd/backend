namespace Domain.Enums;

/// <summary>
/// Specifies the preferred time system for displaying time values.
/// </summary>
public enum TimeSystem
{
    /// <summary>
    /// 12-hour format with AM/PM (e.g., 2:00 PM).
    /// </summary>
    TwelveHour,

    /// <summary>
    /// 24-hour format (e.g., 14:00).
    /// </summary>
    TwentyFourHour,
}
